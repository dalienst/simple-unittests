def add(a, b):
    """
    A function to add two numbers
    """
    return a + b


def multiply(a, b):
    """
    A function to multiply two numbers
    """
    return a * b


def subtract(a, b):
    """
    A function to subtract two numbers
    """
    return a - b


def divide(a, b):
    """
    A function to divide two numbers
    """
    return a / b


def modulus(a, b):
    """
    A function to find modulus of two numbers
    """
    return a % b
